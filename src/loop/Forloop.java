package loop;

public class Forloop {

	public static void main(String[] args) {
		
		// i want to type 1 to 1000 numbers of difference 2
		
		int a= 1;
		
		for ( a = 1; a<=1000; a=a+2) {
			
			System.out.println("we find a "+a);
			
		}
		
		// i want to type 35 table 
		
		int b = 35;
		
		for (b = 35; b<=350; b=b+35) {
			
			System.out.println(b);
			
		}
		

	}

}
